    public void cargarTabla() {
        try {
            DefaultTableModel modelotabla = (DefaultTableModel) TablaCompras.getModel();
            modelotabla.setRowCount(0);
            PreparedStatement ps;
            ResultSet rs;
            ResultSetMetaData rsmd;
            int columnas;

            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("Select* from COMPRAS");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 0; i < columnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelotabla.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

    private void InsertarDatos() {
        // TODO add your handling code here:
        try {
            int idcompra = Integer.parseInt(txtCom.getText());
            int idproduc = Integer.parseInt(txtPro.getText());
            int cantidad = Integer.parseInt(txtCan.getText());
            int total = Integer.parseInt(txtTot.getText());
            String fecha = txtFec.getText().toString();

            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO  COMPRAS (ID_COMPRA, ID_PRODUCTO, CANTIDAD, TOTAL, FECHA) VALUES(?,?,?,?,?)");
            ps.setInt(1, idcompra);
            ps.setInt(2, idproduc);
            ps.setInt(3, cantidad);
            ps.setInt(4, total);
            ps.setString(5, fecha);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Insertado");
            limpiar();
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

    public void limpiar() {
        txtCom.setText("");
        txtPro.setText("");
        txtCan.setText("");
        txtTot.setText("");
        txtFec.setText("");

    }

