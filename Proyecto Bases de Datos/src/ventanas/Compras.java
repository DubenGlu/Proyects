    public void Actualizar() {
        try {
            int idcompra = Integer.parseInt(txtCom.getText());
            int idproduc = Integer.parseInt(txtPro.getText());
            int cantidad = Integer.parseInt(txtCan.getText());
            int total = Integer.parseInt(txtTot.getText());
            String fecha = txtFec.getText();

            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("update COMPRAS SET ID_PRODUCTO=?, CANTIDAD=?, TOTAL=?, FECHA=? where ID_COMPRA=?");

            ps.setInt(1, idproduc);
            ps.setInt(2, cantidad);
            ps.setInt(3, total);
            ps.setString(4, fecha);
            ps.setInt(5, idcompra);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Modificado");
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

