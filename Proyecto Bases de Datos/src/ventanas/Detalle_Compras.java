    public void cargarTabla() {
        try {
            DefaultTableModel modelotabla = (DefaultTableModel) TablaDetalle_Compras.getModel();
            modelotabla.setRowCount(0);
            PreparedStatement ps;
            ResultSet rs;
            ResultSetMetaData rsmd;
            int columnas;

            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("Select* from DETALLE_COMPRAS");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 0; i < columnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelotabla.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

    private void InsertarDatos() {
        // TODO add your handling code here:
        try {
            int compra = Integer.parseInt(txtCom.getText());
            int empleado = Integer.parseInt(txtEmp.getText());
            int proveedor = Integer.parseInt(txtPro.getText());

            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO  DETALLE_COMPRAS (ID_COMPRA, ID_EMPLEADO,ID_PROVEEDOR) VALUES(?,?,?)");
            ps.setInt(1, compra);
            ps.setInt(2, empleado);
            ps.setInt(3, proveedor);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Insertado");
            limpiar();
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

    public void limpiar() {
        txtCom.setText("");
        txtEmp.setText("");
        txtPro.setText("");

    }

