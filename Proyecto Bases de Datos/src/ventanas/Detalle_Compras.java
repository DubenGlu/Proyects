    // TODO add your handling code here:
    public void Actualizar() {
        try {

            int compra = Integer.parseInt(txtCom.getText());
            int empleado = Integer.parseInt(txtEmp.getText());
            int proveedor = Integer.parseInt(txtPro.getText());

            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("update DETALLE_COMPRAS SET ID_EMPLEADO=?,ID_PROVEEDOR=? where ID_COMPRA=?");
            ps.setInt(1, empleado);
            ps.setInt(2, proveedor);
            ps.setInt(3, compra);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Modificado");
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

//eliminar
    private void Eliminar() {
        try {
            int id_persona = Integer.parseInt(txtCom.getText());

            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("delete from DETALLE_COMPRAS  where ID_COMPRA=?  ");

            ps.setInt(1, id_persona);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro borrado");
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }

