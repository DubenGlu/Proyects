    private void btn_insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertarActionPerformed
        try {
            int id = Integer.parseInt(txt_idproducto.getText().toString());
            String nomb = txt_nombre.getText().toString();
            int precio = Integer.parseInt(txt_precio.getText().toString());
            int dis = Integer.parseInt(txt_piezas.getText().toString());
            int dep = Integer.parseInt(txt_departamento.getText().toString());
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO  ALMACEN (ID_PRODUCTO, NOMBRE, PRECIO, DISPONIBLES,DEPT) VALUES(?,?,?,?,?)");
            ps.setInt(1, id);
            ps.setString(2, nomb);
            ps.setInt(3, precio);
            ps.setInt(4, dis);
            ps.setInt(5, dep);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro insertado correctamente");
            limpiar();
            cargarTabla();

        } catch (SQLException sql) {
            JOptionPane.showMessageDialog(null, sql.toString(), "", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }
    }//GEN-LAST:event_btn_insertarActionPerformed

